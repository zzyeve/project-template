import Vue from "vue";

const RULES = {
	plusOrMinusNum: /^(^-?\d+$)$/, //正负整数
	account: /^([0-9a-zA-Z]){4,20}$/, //4-20位数字或字母
	passWord: /^(?![0-9]+$)(?![a-zA-Z]+$)[0-9A-Za-z]{6,12}$/, //6-12位数字和字母
	sort: /^[1-9][0-9]{0,2}$/, // 1-999排序
	phone: /^([0-9]){8,20}$/, // 8-20位手机号
	money: /^([0-9]|[1-9][0-9]{0,7})(\.[1-9]|\.[0-9][1-9])?$/, //0-99999999.99
};

Vue.prototype.$RULES = RULES;